The Global Integrated Network of IEEE Members (GINI), is an initiative of the Bangalore section to promote collaboration between the student chapters in the section. As a part of the Dakshin Kannada region, IEEE NITK holds a pivotal role of implementing the ideals of GINI, which includes successful collaborations with neighbouring institutes for resource sharing and information interchange. IEEE NITK is the largest student chapter in the Bangalore Section and is the leader of the Mangalore sub-section.<br /><br>


