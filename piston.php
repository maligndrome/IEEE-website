<font style="color:#18539b;">Piston</font> is a special interest group dedicated to the engineering applications of Mechanical, Automation, Civil, Chemical and Material Sciences. The SIG functions as a platform for students interested in learning and advancing specific areas of knowledge and technology. The SIG members co-operate, communicate, and organise meets, talks and conferences to discuss latest technology advancement and application. The SIG talks and seminars are given for encompassing the real world applications, which bring together all eager learners. Workshops are regularly held to equip members with software skills like MATLAB, ANSYS, CATIA, AutoCAD etc. The SIG is an excellent forum to interact with experienced seniors and professionals. <br>

Piston SIG collaborates with other SIG’s i.e. Diode and Python in learning and advancing in interdisciplinary areas such as Robotics, Mechatronics, Microcontrollers and Automation. The SIG also collaborates with SIGHT Chapter to discuss possible technological solutions for humanitarian application.<br>

The SIG consists of 2 societies namely Mach (Mechanical, Automation and Chemical Engineering) and RAS (Robotics and Automation). The specific areas of technology in Piston can be classified as follows -<br>
<ul>
 
<li>	Aerospace Engineering
<li>	Automobile engineering
