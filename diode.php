<font style="font-size:18px">
<font style="color:#18539b;">The Circuits and Systems (CAS)</font> group, also known as <i>Diode</i> consists of a team of students with a strong interest in the field of Electronics and Electrical Science. There are various seminars and talks organised by this SIG to encourage students to learn and gain more knowledge in their particular field of interest. CAS has contributed greatly to the development of interest in the field of robotics and microcontrollers. Tutorials for microcontroller programming and software like MATLAB and PSpice, are a regular part of Diode's activities. Various sessions held have had eager teams discussing robotics and other intriguing problem statements. The main focus of the CAS is on small projects, a cross-year collaborative effort among all the members that will help in academics and give us an edge when participating in various technical competitions conducted in many prestigious institutes.<br />
  </font>  
<br>
<br>
<font style="color:#18539b; font-size:30px;">Projects</font><br>
<br>
<font style="color:#18539b; font-size:18px;"><b>
Heart rate monitoring during physical exercise using PPG signals.
</b>
</font><br>

A photoplethysmogram (PPG) is an optically obtained plethysmogram, a volumetric measurement of an organ.The goal of this project is to estimate heart rates from PPG signals recorded from subjects' wrist when the subjects are performing various physical exercise, such as fast running, or jumping.This project is taken as a part of 2015 IEEE Signal Processing Cup(http://www.zhilinzhang.com/spcup2015/) conducted by Signal Processing Society.<br>
<br>
<font style="color:#18539b; font-size:18px;"><b>
Smart Grid /Grid connected solar system
</b></font><br>

A solar panel feeds a local load through a variable dc to ac inverter. If there is excess power generated it is synchronized and fed back to the electrical grid using PLL. Apart from this there is also a battery charging unit which can be used for other purposes<br>
<br>

<font style="color:#18539b; font-size:18px;"><b>
Synthetic aperture Radar
</b></font><br>

Building a Radar using copper tin cans. Laptop-based radar sensor capable of measuring Doppler, range, and forming synthetic aperture radar (SAR) images.<br>
<br>

<font style="color:#18539b; font-size:18px;"><b> Nerve Locator
</b></font><br>

The device is intended for stimulation of motor nerves to locate nerves and test nerve excitability. Such devices are commonly used during surgeries. Although such devices can be bought, they are mostly not available in India and are expensive.<br>
<br>
<font style="color:#18539b; font-size:18px;"><b>
Alcohol Detection System for Prevention of Drinking-and-Driving
</b></font><br>

We propose to prevent drinking-and-driving by installing a system in cars/bikes that will first analyze the breath of the driver before allowing the driver to turn on the car/bike. If the breath indicates a level of alcohol which is not favorable for driving safely, the system will not allow the vehicle to be turned on thereby preventing him from driving. The system consists of an alcohol sensor which is fitted on to a wearable device.
<br>
<br>

<font style="color:#18539b; font-size:18px;"><b>Sensitive Hands
</b></font><br>

It is related to the people suffering from anacusis or deafness (hearing impaired people).This projects deals with building a device which can help people sense sound, when we say sense sound that is you might actually be able to hear through touch! That is not to just feel the vibration of sounds, like touching a speaker, but to feel sound. This project is a small step taken towards designing and building a better, efficient and interactive hearing aid.<br>
<br>

<font style="color:#18539b; font-size:18px;"><b>
Guide Stick for the Visually Impaired
</b></font><br>

The guide stick for the visually impaired aims to be a tool which would help the user gauge obstacles in his immediate surroundings by providing an audio feedback.<br>
<br>

<font style="color:#18539b; font-size:18px;"><b>
Pressure Plate
</b></font><br>

To build an electronic plate with pressure sensors for the doctor so as to estimate the pressure applied by each limb and its fingers. The plate consists of an LCD display to compare the pressure applied by a common man/Doctor and the patient.
<br>
<br>

<font style="color:#18539b; font-size:18px;"><b>
Suture Strength measurement
</b></font><br>

The project involves building a device to measure the strength of a sample suture(artificial tendon replacement) could tolerate by applying stress across it. The device will have to provide a continuous reading of the stress applied and record it when the suture breaks. So, it will help doctors to decide in recommending sutures for patients<br>
<br>
<font style="color:#18539b; font-size:18px;"><b>
Nerve Fibre joining simulator
</b></font><br>

The project involves building a system to test and train medical students to join a broken nerve fibre. A nerve fibre contains a set of 6-8 bundles which need to be aligned symmetrically to get a correct joint,for the message signal to travel across the nerve bundles.The corrected joint doesn't require the nerve ends to touch. So the system will have to simulate these nerve fibres and quantify the error in the joint made i.e how much of the message signal may flow across the nerve fibre.Each bundle of nerves will have to face the correct bundle on the other face of the nerve fault.
<br>
<br>
<font style="color:#18539b; font-size:18px;"><b>
Backbone curve quantification
</b></font><br>

Every individual's backbone has a curved structure and a curvature when he/she stands against the wall. This project involves making a product to find out this curve for any individual, give a graphical output about the curve and quantise it,i.e make it comparable to standard healthy backbone curve shapes. This information can help diagnose any backbone imperfections and allow the surgeon to use appropriate surgery methods.
<br>
<br>

<font style="color:#18539b; font-size:18px;"><b>
Walk characteristics logging device
</b></font>
The project involves building a system to log,display and quantify the shoulder tilt, sole contact with the ground(time the feet sole is in contact with the ground for) to diagnose walking disorders and analyse treatment results.
<br>
<br>

<font style="color:#18539b; font-size:18px;"><b>
Compartmental Pressure Measurement
</b></font><br>

Determining the pressure between various muscles inside the Human Body. We plan to build a Electronic Barometer and record values.
<br>
<br>

<font style="color:#18539b; font-size:18px;"><b>Smart Slate
</b></font><br>
This project aims at building a smart slate for the visually challenged that enables them to feel 3d projections embossed out of the refreshable display contained in the slate. These 3d projections can be representations of different terrains, shapes, etc. This project emphasizes on the aspect of 'sense and feel' based learning which may be used by the visually challenged to effectively visualize figures.This project was a hardware implementation of a Paper on tactile interfaces for the blind by students of our student branch, accepted at International Humanitarian Technology Conference 2014 (IHTC'14)
<br>
<br>

<font style="color:#18539b; font-size:18px;"><b>
QUADCOPTER</b></font><br>

Quadcopter is an unmanned air vehicle propelled by 4 motors. This project was initially taken up to  give  us  a  practical  approach  to  implementing  and  gauging  control  system  algorithms.  The project  requires  an  understanding  in  the  fields  of  control  engineering,  microcontrollers,  signal processing, power electronics and basics of mechanical engineering.
<br>
<br>

<font style="color:#18539b; font-size:18px;"><b>
HOME AUTOMATION
</b></font><br>

The aim of the project is to develop an affordable, comprehensive and user­friendly automation system .This will not only allow the user to control and monitor the large number of gadgets and appliances in a house but also provide the highest level of security possible. The aim is to make it  affordable  by  reducing  the  hardware  components  involved  and  supplement  it  with  just  as effective codes using image processing and other signal processing techniques and algorithms.
<br>
<br>

<font style="color:#18539b; font-size:18px;"><b>
COILGUN:
</b></font><br>

The coil gun is essentially a linear projectile accelerator that uses a magnetic field to accelerate a  projectile  (bullet)  through  the  barrel  of  the  coil  gun.  A  SMPS  is  interfaced  with  a  designed analog circuitry to a capacitor bank ohaving a charging capability of up to 350V.
<br>
<br>

<font style="color:#18539b; font-size:18px;"><b>
Kinect Human Following Bot
</b></font><br>

This project involved building a human follower bot using the depth camera sensor of the Microsoft Kinect. Robot Operating System was used as the platform for interfacing with the Kinect and the powerful packages of ROS were used as a skeleton to build the code.
<br>
<br>

<font style="color:#18539b; font-size:18px;"><b>
Ethernet to embedded system interfacing device (Wiznet platform)
</b></font><br>

The aim of the project was to make an interfacing system which control an embedded micro-controller via a simple web page hosted on it through a WIZNET W5100 IC which acts like a server connected to the LAN network.
<br>
<br>

<font style="color:#18539b; font-size:18px;"><b>
Motion controlled interfacing device (“Keyglove”)
</b></font><br>

The project involves making a key glove for achieving hand motion control input method using accelerometer and gyro sensor placed on the hand and interfacing it with a number of applications which used and processed values from the Keyglove.
The applications developed were:
<ul>
<li><b>Labyrinth</b>: This project involves interfacing the keyglove with a labyrinth(wireless) for 
translating the input to visible outputs in the form of motion of the labyrinth platform.
<li><b>Mouse cursor control</b>: This project involves interfacing the keyglove with the laptop (wireless) and using the input from the keyglove to control the mouse. The motion of the mouse cursor depends on the magnitude of tilt due to the movement of the hand vertically (for y axis motion) and horizontally (for x axis motion).
<li><b>Virtual 3D object control</b>: This involves feeding values from the keyglove to a program created using OpenGL. The 3D object created rotates in space according to these values.
</ul>
